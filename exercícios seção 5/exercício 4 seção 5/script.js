// Função Math.pow() exibe a potencia de um número
//teste a função com console.log() e as seguintes bases: 2,3,18 e o expoente deve ser 2
//Recebe 2 argumentos, base e expoente
//ex: Math.pow(5,3)

console.log(Math.pow(2,2));
console.log(Math.pow(3,2));
console.log(Math.pow(18,2));

//outra forma de fazer é declarando a base com const

const num = 18;

console.log(Math.pow(2,2));
console.log(Math.pow(3,2));
console.log(Math.pow(num,2));